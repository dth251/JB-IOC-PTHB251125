-- Thực thể chính trong hệ thống là : Sách, Độc Giả , Phiếu mượn sách
-- Các thuộc tính của thực thể sách là : Tên sách, mã ISBN, tác giả, năm xuất bản, thể loại
-- Các thuộc tính của thực thể độc giả là : mã độc giả, họ tên, ngày sinh, địa chỉ, số điện thoại
-- Các thuộc tính cảu thực thể phiếu mượn sách : mã phiếu, ngày mượn, ngày trả dự kiến, trạng thái

-- Khóa chính ( Primary key) là: mã ISBN, mã độc giả, mã phiếu
-- khóa ngoại (foreign key) là : mã ISBN, mã độc giả.
